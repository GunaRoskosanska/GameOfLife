namespace GameOfLife.Models
{
    /// <summary>
    /// There are 3 game options
    /// </summary>
    public enum GameOption
    {
        /// <summary>
        /// Option of the game - start new game
        /// </summary>
        NewGame = 1,
        /// <summary>
        /// Option of the game - continue previous game
        /// </summary>
        ContinuePreviousGame = 2,
        /// <summary>
        /// Option of the game - exit game
        /// </summary>
        Exit = 3
    }
}
